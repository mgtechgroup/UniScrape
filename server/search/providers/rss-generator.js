import { load } from 'cheerio'
import { fetchWithRetry } from './proxy-rotator.js'

/**
 * RSS Generator - converts any scraped page into RSS feed
 * Integrated from html2rss patterns
 */
export async function generateRSS(url, options = {}) {
  const { 
    titleSelector = 'h1, h2, .title, [itemprop="name"]',
    itemSelector = 'article, .item, .video, .post, .entry, .card, .result',
    itemTitleSelector = 'h2, h3, .title, a',
    itemLinkSelector = 'a',
    itemDateSelector = 'time, .date, .published',
    itemDescSelector = '.description, .summary, .content, p',
    maxItems = 20,
  } = options

  try {
    const html = await fetchWithRetry(url)
    const $ = load(html)
    
    const feedTitle = $(titleSelector).first().text().trim() || url
    const items = []

    $(itemSelector).each((i, el) => {
      if (i >= maxItems) return false
      
      const $el = $(el)
      const title = $el.find(itemTitleSelector).first().text().trim()
      let link = $el.find(itemLinkSelector).first().attr('href') || ''
      if (link && !link.startsWith('http')) link = new URL(link, url).href
      const date = $el.find(itemDateSelector).first().text().trim() || $el.find(itemDateSelector).first().attr('datetime') || ''
      const desc = $el.find(itemDescSelector).first().text().trim().substring(0, 300)

      if (title) {
        items.push({ title, link, date, description: desc })
      }
    })

    return buildRSSXML(feedTitle, url, items)
  } catch (e) {
    return `<?xml version="1.0"?><rss version="2.0"><channel><title>Error</title><description>${e.message}</description></channel></rss>`
  }
}

function buildRSSXML(title, link, items) {
  const itemsXML = items.map(i => `
    <item>
      <title>${escapeXML(i.title)}</title>
      <link>${escapeXML(i.link)}</link>
      ${i.date ? `<pubDate>${escapeXML(i.date)}</pubDate>` : ''}
      ${i.description ? `<description>${escapeXML(i.description)}</description>` : ''}
      <guid>${escapeXML(i.link || i.title)}</guid>
    </item>
  `).join('')

  return `<?xml version="1.0" encoding="UTF-8"?>
<rss version="2.0" xmlns:atom="http://www.w3.org/2005/Atom">
  <channel>
    <title>${escapeXML(title)}</title>
    <link>${escapeXML(link)}</link>
    <description>RSS feed generated by UniScrape</description>
    <atom:link href="${escapeXML(link)}" rel="self" type="application/rss+xml"/>
    <generator>UniScrape RSS Generator</generator>
    <lastBuildDate>${new Date().toUTCString()}</lastBuildDate>
    ${itemsXML}
  </channel>
</rss>`
}

function escapeXML(str) {
  return str.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;').replace(/'/g, '&apos;')
}
